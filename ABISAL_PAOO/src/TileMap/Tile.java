package TileMap;

import java.awt.image.BufferedImage;

public class Tile {
	
	private BufferedImage image;
	private int type;
	
	// tile types
	public static final int NORMAL = 0;//asta inseamna ca dala e "transparenta"
	public static final int BLOCKED = 1;//asta inseamna ca poti trece peste dala, adica nu e "trasparenta"
	
	public Tile(BufferedImage image, int type) {
		this.image = image;
		this.type = type;
	}
	
	public BufferedImage getImage() { return image; }
	public int getType() { return type; }
	
}
